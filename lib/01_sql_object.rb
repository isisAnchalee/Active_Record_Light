class SQLObject
  def self.columns

    query = <<-SQL
    SELECT *
    FROM "#{table_name}"
    SQL

    tables = DBConnection.execute2(query)
    @columns = tables.first.map(&:to_sym)
  end

  def self.finalize!
    self.columns.each do |col|
      define_method(col) do
        self.attributes[col]
      end

      define_method("#{col}=") do |value|
        self.attributes[col] = value
      end
    end
  end

